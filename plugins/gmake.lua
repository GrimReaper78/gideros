-- write to file and output to console
local write = function(file, str)
  file:write(str .."\n")
  print(str)
end

--
local comments = {}
comments.header = [[##BEGIN##
##CONT##     autogenerated by gmake.lua
##END##

]]

local function writeHeader(file, _type)
  local str = comments.header
  _type = _type:upper()
  if     _type == "#" then
    str = str:gsub("##BEGIN##", "#")
    str = str:gsub("##CONT##", "#")
    str = str:gsub("##END##", "#")
  elseif _type == "XML" then
    str = str:gsub("##BEGIN##", "<!--")
    str = str:gsub("##CONT##", "  ")
    str = str:gsub("##END##", "-->")
  elseif _type == "C" or _type == "CXX" or _type == "H" or _type == "HPP" then
    str = str:gsub("##BEGIN##", "/*")
    str = str:gsub("##CONT##", "  ")
    str = str:gsub("##END##", "*/")
  else
    return
  end
  write(file, str)
end

-- for maintaining nice autogenerated indentation
local getPaddedString = function(str, target_length)
  if #str >= target_length then return str end
  return str .. string.rep(" ", target_length - #str)
end

-- get nice string from table, with table entries prefixed and suffixed and
-- separated by custom separator (eg space or comma)
local function getSepStringFromTable(t, separator, prefix, suffix)
  local s = ""
  prefix = prefix and prefix or ""
  suffix = suffix and suffix or ""
  for k, v in ipairs(t) do
    s = s .. prefix .. v .. suffix .. separator
  end
  return s
end

-- one shot file creation function
local function createFileWithContent(dir, file_name, content, header_type)
  local file = io.open(dir .. os.sep .. file_name, "w")
  if not file then return false end
  print("\n\nWriting file.. " .. dir .. os.sep .. file_name .. "\n")
  if header_type then writeHeader(file, header_type) end
  file:write(content)
  file:close()
  print(content)
  return true
end

--
local function appendToFile(dir, file_name, content)
  local file = io.open(dir .. os.sep .. file_name, "a")
  if not file then return false end
  file:write(content)
  file:close()
  return true
end

--
local function createDirectory(dir)
  print("Creating directory:", dir)
  local command = "mkdir " .. dir
  print(command)
  os.execute(command)
end

--
local function getPathFilenameAndExt(full_file_name_with_path_and_extension)
  local f = full_file_name_with_path_and_extension
  local path, name, ext
  path = f:match("^(.+/)")
  path = path and path or ""
  name = f:match("/?([%w_%-_]+)%.")
  name = name and name or ""
  ext = f:match("(%.[%.%w%-_]+)$")
  ext = ext and ext or ""
  return path, name, ext
end

